package operators;

public class Comparsion_Operators {
    public static void main(String[] args) {
        int x = 5, y = 3;
        
        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
    }
    
}
