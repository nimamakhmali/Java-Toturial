package Conditional_Statements;

public class IfElse {
    public static void main(String[] args) {
        int number = 0;
        if (number > 0) {
            System.out.println("number is +");
        } 
        else if (number < 0) {
            System.out.println("number is -");
        } 
        else {
            System.out.println("number is Zero");
        }        
    }
}
